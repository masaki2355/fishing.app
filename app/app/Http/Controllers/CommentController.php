    public function ajaxComment(Request $request){
        $body = $request->body;
        $user_id = Auth::id();
        $post_id = $request->post_id;
        $comments = Comment::create([
            'body' => $body,
            'user_id' => $user_id,
            'post_id' => $post_id,
        ]);
        $json = ["comments" => $comments];
        return response()->json($json);

    }

